package main

import "log"

func fib(num int) int  {
	if num ==0 || num==1{
		return 1
	}
	return fib(num-2) + fib(num-1)

}

func main()  {
	res1 := fib(7)
	log.Println(res1)
	for i:=0;i<10;i++{
		log.Printf("[%d=%d]",i,fib(i))
	}

}

